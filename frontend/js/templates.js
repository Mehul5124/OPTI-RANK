document.addEventListener("DOMContentLoaded", () => {
  const categoryTabs = document.querySelectorAll(".tab-btn");
  const templateCards = document.querySelectorAll(".template-card");
  const modal = document.getElementById("templateModal");
  const modalTitle = document.getElementById("modalTitle");
  const modalBody = document.getElementById("modalBody");
  const closeModal = document.querySelector(".close-modal");
  const logoutBtn = document.getElementById("logoutBtn");

  let currentTemplate = "";

  // Check authentication
  const token = localStorage.getItem("token");
  if (!token) {
    alert("Please login to access templates");
    window.location.href = "login.html";
    return;
  }

  // Category filtering
  categoryTabs.forEach(tab => {
    tab.addEventListener("click", () => {
      categoryTabs.forEach(t => t.classList.remove("active"));
      tab.classList.add("active");

      const category = tab.dataset.category;
      filterTemplates(category);
    });
  });

  // Logout
  if (logoutBtn) {
    logoutBtn.addEventListener("click", () => {
      localStorage.removeItem("token");
      localStorage.removeItem("username");
      alert("Logged out successfully!");
      window.location.href = "login.html";
    });
  }

  // Close modal
  closeModal.addEventListener("click", () => {
    modal.style.display = "none";
  });

  window.addEventListener("click", (e) => {
    if (e.target === modal) {
      modal.style.display = "none";
    }
  });

  // Filter templates by category
  function filterTemplates(category) {
    templateCards.forEach(card => {
      if (category === "all") {
        card.style.display = "block";
      } else if (card.dataset.category === category) {
        card.style.display = "block";
      } else {
        card.style.display = "none";
      }
    });
  }
});

// Use template - Show in modal
function useTemplate(templateId) {
  const template = templates[templateId];
  const modal = document.getElementById("templateModal");
  const modalTitle = document.getElementById("modalTitle");
  const modalBody = document.getElementById("modalBody");

  if (template) {
    modalTitle.textContent = template.title;
    modalBody.innerHTML = `<pre>${template.content}</pre>`;
    modal.style.display = "block";
    
    // Store current template for copy/download
    window.currentTemplateContent = template.content;
    window.currentTemplateTitle = template.title;
  }
}

// Copy template to clipboard
function copyTemplate() {
  const content = window.currentTemplateContent;
  
  navigator.clipboard.writeText(content).then(() => {
    alert("‚úÖ Template copied to clipboard!");
  }).catch(err => {
    console.error("Failed to copy:", err);
    alert("‚ùå Failed to copy. Please try again.");
  });
}

// Download template as text file
function downloadTemplate() {
  const content = window.currentTemplateContent;
  const title = window.currentTemplateTitle;
  const filename = title.toLowerCase().replace(/\s+/g, '-') + '.txt';
  
  const blob = new Blob([content], { type: 'text/plain' });
  const url = window.URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  window.URL.revokeObjectURL(url);
  
  alert(`‚úÖ Template downloaded as ${filename}`);
}
